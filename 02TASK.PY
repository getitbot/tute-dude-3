import math

num =  float(input("Enter the number: "))

Square_root = math.sqrt(num)
Natural_logaritham = math.log(num)
Sine = math.sin(num)

print(f"The square root of {num} is {Square_root}")
print(f"the Logarithm of {num} is {Natural_logaritham}")
print(f"The sine of the {num} in radians is {Sine}")
